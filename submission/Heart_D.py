import pandas as pd
import numpy as np

from sklearn.model_selection import train_test_split, StratifiedKFold, GridSearchCV
from sklearn.preprocessing import StandardScaler
from sklearn.pipeline import Pipeline
from sklearn.impute import SimpleImputer

from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import classification_report, f1_score, accuracy_score

import joblib

# ----------------------------
# 1. Load Data (IMPORTANT FIX)
# ----------------------------
df = pd.read_csv("train .csv")   # your actual filename

# ----------------------------
# 2. Split Features & Target
# ----------------------------
X = df.drop("target", axis=1)
y = df["target"]

# ----------------------------
# 3. Train-Test Split (Stratified)
# ----------------------------
X_train, X_val, y_train, y_val = train_test_split(
    X, y, test_size=0.2, stratify=y, random_state=42
)

# ----------------------------
# 4. Pipeline (BEST PRACTICE)
# ----------------------------
pipeline = Pipeline([
    ("imputer", SimpleImputer(strategy="median")),
    ("scaler", StandardScaler()),
    ("model", RandomForestClassifier(random_state=42))
])

# ----------------------------
# 5. Hyperparameter Tuning
# ----------------------------
param_grid = {
    "model__n_estimators": [100, 200, 300],
    "model__max_depth": [None, 5, 10],
    "model__min_samples_split": [2, 5],
    "model__min_samples_leaf": [1, 2],
    "model__class_weight": ["balanced"]
}

cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

grid = GridSearchCV(
    pipeline,
    param_grid,
    scoring="f1",     # IMPORTANT for leaderboard
    cv=cv,
    n_jobs=-1,
    verbose=2
)

# ----------------------------
# 6. Train Model
# ----------------------------
grid.fit(X_train, y_train)

best_model = grid.best_estimator_

print("Best Parameters:", grid.best_params_)

# ----------------------------
# 7. Evaluation
# ----------------------------
preds = best_model.predict(X_val)

print("\nF1 Score:", f1_score(y_val, preds))
print("Accuracy:", accuracy_score(y_val, preds))
print("\nClassification Report:\n", classification_report(y_val, preds))

# ----------------------------
# 8. Save Model
# ----------------------------
joblib.dump(best_model, "best_heart_model.pkl")

